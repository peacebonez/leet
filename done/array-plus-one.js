const plusOne = function (digits) {
  let i = digits.length - 1;
  const recurser = (arr) => {
    if (arr[i] !== 9) {
      arr[i] += 1;
      return arr;
    }
    arr[i] = 0;
    if (i > 0) i--;
    else digits.unshift(0);

    return recurser(arr);
  };
  
  recurser(digits);

  return digits;
};

